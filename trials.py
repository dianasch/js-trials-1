def get_all_evens(nums):
    # Create an empty list to store even nums
    even_nums = []

    # Loop through each number in nums
    for num in nums:

        # Determine if number is even
        if num % 2 == 0:

            # If so, append to list
            even_nums.append(num)
    
    # Return list
    return even_nums


